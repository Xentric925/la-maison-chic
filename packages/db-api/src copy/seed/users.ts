export const seedUsers = async () => {
  await writePrisma.$transaction(async (prisma) => {
    await Promise.all(
      users.map((user) =>
        prisma.user.create({
          data: {
            firstName: user.firstName,
            lastName: user.lastName,
            email: user.email,
            locationId: user.locationId,
            role: user.role as UserRole,
            reportsToId: user.reportsToId,
          },
        }),
      ),
    );
  });

  seedUserRelations();
};

const seedUserRelations = async () => {
  usersDepartments.forEach(async (userDepartment) => {
    await writePrisma.userDepartment.create({
      data: {
        ...userDepartment,
      },
    });
  });
  usersTeams.forEach(async (userTeam) => {
    await writePrisma.userTeam.create({
      data: {
        ...userTeam,
      },
    });
  });
  usersGroups.forEach(async (userGroup) => {
    await writePrisma.userGroup.create({
      data: {
        ...userGroup,
      },
    });
  });
};
